import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';

const repoRoot = process.cwd();
const blogDir = path.join(repoRoot, 'src', 'blog');
const outputFile = path.join(blogDir, 'generatedManifest.js');

function extractMetaLiteral(source) {
  const exportIndex = source.indexOf('export const meta');
  if (exportIndex === -1) {
    throw new Error('Missing `export const meta` export');
  }

  const objectStart = source.indexOf('{', exportIndex);
  if (objectStart === -1) {
    throw new Error('Could not find meta object start');
  }

  let depth = 0;
  let stringQuote = null;
  let escaped = false;

  for (let index = objectStart; index < source.length; index += 1) {
    const char = source[index];

    if (stringQuote) {
      if (escaped) {
        escaped = false;
        continue;
      }

      if (char === '\\') {
        escaped = true;
        continue;
      }

      if (char === stringQuote) {
        stringQuote = null;
      }

      continue;
    }

    if (char === '"' || char === "'" || char === '`') {
      stringQuote = char;
      continue;
    }

    if (char === '{') {
      depth += 1;
    } else if (char === '}') {
      depth -= 1;
      if (depth === 0) {
        return source.slice(objectStart, index + 1);
      }
    }
  }

  throw new Error('Could not find meta object end');
}

function loadMetaFromFile(filePath) {
  const source = fs.readFileSync(filePath, 'utf8');
  const metaLiteral = extractMetaLiteral(source);
  return vm.runInNewContext(`(${metaLiteral})`);
}

function normalizeMeta(fileName, meta = {}) {
  const slug = fileName.replace(/\.mdx$/, '');
  return {
    slug,
    title: meta.title || slug,
    date: meta.date || '',
    summary: meta.summary || meta.excerpt || '',
    tags: Array.isArray(meta.tags) ? meta.tags : [],
    readingTime: typeof meta.readingTime === 'number' ? meta.readingTime : null,
    featured: Boolean(meta.featured),
    lastModified: meta.lastModified || '',
    fileName,
  };
}

const posts = fs
  .readdirSync(blogDir)
  .filter((fileName) => fileName.endsWith('.mdx'))
  .sort()
  .map((fileName) => normalizeMeta(fileName, loadMetaFromFile(path.join(blogDir, fileName))));

const postEntries = posts
  .map(
    (post) => `  {
    slug: ${JSON.stringify(post.slug)},
    title: ${JSON.stringify(post.title)},
    date: ${JSON.stringify(post.date)},
    summary: ${JSON.stringify(post.summary)},
    tags: ${JSON.stringify(post.tags)},
    readingTime: ${JSON.stringify(post.readingTime)},
    featured: ${JSON.stringify(post.featured)},
    lastModified: ${JSON.stringify(post.lastModified)},
    fileName: ${JSON.stringify(post.fileName)},
    load: () => import(${JSON.stringify(`./${post.fileName}`)}),
  }`
  )
  .join(',\n');

const output = `// This file is auto-generated by scripts/generate-blog-manifest.js.
// Do not edit it manually.

export const blogPosts = [
${postEntries}
].sort((a, b) => (b.date || '').localeCompare(a.date || ''));

const blogPostMap = new Map(blogPosts.map((post) => [post.slug, post]));

export function getBlogPostEntry(slug) {
  return blogPostMap.get(slug) || null;
}

export function getRecentBlogPosts(limit = 3) {
  return blogPosts.slice(0, limit);
}
`;

fs.writeFileSync(outputFile, output);
